'use client';

import * as React from 'react';

import { cn } from '@/lib/ui-utils';

import {
  cardVariants,
  cardHeaderVariants,
  cardTitleVariants,
  cardDescriptionVariants,
  cardContentVariants,
  cardFooterVariants,
} from './variants';

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(cardVariants(), className)}
    {...props}
  />
));
Card.displayName = 'Card';

